const int MAX = 1e5;
int divi[MAX];
vector<int> primes;

void crivo(int lim) {
	divi[1] = 1;
	for (int i = 2; i <= lim; i++) {
		if (divi[i] == 0) divi[i] = i, primes.push_back(i);
		for (int j : primes) {
			if (j > divi[i] or i*j > lim) break;
			divi[i*j] = j;
		}
	}
}

void solve(){
    int n;
    cin >> n;

    int ans = 1;
    for(auto p : primes){
        if(n % p == 0){
            ans *= p;

            while(n % p == 0) n /= p;
        }
    }
    ans *= n;

    cout << ans << endl;
}


signed main(){
    crivo(MAX - 1);
    darvem;
    int t = 1;
    cin >> t;

    while(t--) solve();
}